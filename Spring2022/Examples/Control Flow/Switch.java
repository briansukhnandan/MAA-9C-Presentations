class Switch {
  
  public static void main(String[] args) {

    String day = "Monday";

    switch(day) {
        
      case "Monday":
        System.out.println("It's Monday :(");
        break;

      case "Tuesday":
        System.out.println("It's Tuesday :/");
        break;

      case "Wednesday":
        System.out.println("It's Wednesday :3");
        break;

      case "Thursday":
        System.out.println("It's Thursday :)");
        break;

      case "Friday":
        System.out.println("It's Friday :D");
        break;
        
    }
    
  }
  
}