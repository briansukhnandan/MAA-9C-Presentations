class Integers {
  public static void main(String[] args) {
    
    // Let's make 2 integers.
    int my_first_integer = 5;
    int my_second_integer = 6;

    // We can add integers!
    int sum_of_my_numbers = my_first_integer + my_second_integer;
    int product_of_my_numbers = my_first_integer * my_second_integer;
    int difference_of_my_numbers = my_first_integer - my_second_integer;

    // Let's print!
    System.out.println(sum_of_my_numbers);
    System.out.println(product_of_my_numbers);
    System.out.println(difference_of_my_numbers);
    
  }
}