class Floats {
  public static void main(String[] args) {
    
    // Let's make 2 floats.
    float my_first_float = 6.73f;
    float my_second_float = 3.45f;

    // We can add floats!
    float sum_of_my_floats = my_first_float + my_second_float;
    float product_of_my_floats = my_first_float * my_second_float;
    float difference_of_my_floats = my_first_float - my_second_float;

    // Let's print!
    System.out.println(sum_of_my_floats);
    System.out.println(product_of_my_floats);
    System.out.println(difference_of_my_floats);
    
  }
}